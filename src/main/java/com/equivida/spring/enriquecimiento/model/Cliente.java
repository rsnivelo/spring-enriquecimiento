package com.equivida.spring.enriquecimiento.model;

public class Cliente {

	private Integer id;
	private String cedula;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCedula() {
		return cedula;
	}

	public void setCedula(String cedula) {
		this.cedula = cedula;
	}

 }
